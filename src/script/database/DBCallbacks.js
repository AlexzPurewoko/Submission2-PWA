 /**
  * This module is a callback class.
  * Provide by DatabaseHelper to notify all registered this callbacks
  * Which notified by observed class to update their UI
  * Because of database changed
  */

class DBCallbacks {
    constructor() {
        this._callbacks = null;
    }

    set callbacks(nCallbacks){
        this._callbacks = nCallbacks;
    }
    onDataChanged(db){
        if(this._callbacks)
            this._callbacks(db);
    }
}

export default DBCallbacks;